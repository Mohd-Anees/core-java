package simplyLearns;

public class ImplicitDataTypesConversions {
	public static void main(String[] args) {
		int a=100;
		System.out.println("Integer types reperesenaion value:"+a);
		
		long l=a;
		System.out.println("Long types reperesenaion value:"+l);
		
		float f=l;
		System.out.println("Float types reperesenaion value:"+f);
		
		
	}

}
