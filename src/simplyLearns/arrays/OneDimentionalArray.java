package simplyLearns.arrays;

public class OneDimentionalArray {
	public static void main(String[] args) {
		int[] arr;
		arr=new int[5];
		arr[0]=1;
		arr[1]=5;
		arr[2]=9;
		arr[3]=10;
		arr[4]=20;
		
		System.out.println(arr[0]);
		System.out.println(arr[1]);
		System.out.println(arr[2]);
		System.out.println(arr[3]);
	}

}
