package simplyLearns.arrays;

public class ArrayDemo {
	public static void main(String[] args) {
		//declare the variable
		int[] arr;
		//assign elements
		arr=new int[6];
		
		
		//Initialed
		arr[0]=12;
		arr[1]=23;
		arr[2]=34;
		arr[3]=23;
		arr[4]=54;
		arr[5]=100;
		
		//access the elements
		
		System.out.println("Access 1st elements :"+arr[0]);
		System.out.println("Access 2nd elements :"+arr[1]);
		System.out.println("Access 3rd elements :"+arr[2]);
		System.out.println("Access 4th elements :"+arr[3]);
		System.out.println("Access 5th elements :"+arr[4]);
		System.out.println("Access 6th elements :"+arr[5]);
		
		
		
		
	}

}
