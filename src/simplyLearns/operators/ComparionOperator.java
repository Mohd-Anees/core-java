package simplyLearns.operators;

public class ComparionOperator {
	public static void main(String[] args) {
		int n1=12;
		int n2=23;
		
		if(n1==n2) {
			System.out.println("n1==n2");
		}
		
		if(n1>n2) {
			System.out.println("n1>n2");
		}
		if(n1>=n2) {
			System.out.println("n1>=n2");
		}
		
		if(n1<n2) {
			System.out.println("n1<n2");
		}
		
		if(n1<=n2) {
			System.out.println("n1<=n2");
		}
		if(n1!=n2) {
			System.out.println("n1!=n2");
		}
	}

}
